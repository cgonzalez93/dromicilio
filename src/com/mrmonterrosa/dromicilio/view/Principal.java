    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.txt", "txt");//filtro de archivo
        fileChooser.setFileFilter(filter);
        fileChooser.showOpenDialog(this);//abrir dialogo
        File file = fileChooser.getSelectedFile();
        if (file != null) {
            smovimiento = CargarArchivo.load_file(file.getAbsolutePath());
            this.txaInformacion.setText(smovimiento);
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

